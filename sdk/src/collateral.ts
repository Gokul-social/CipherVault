/**
 * Loads the collateral_vault Anchor program. In a real deployment, the IDL
 * would be fetched from the chain or imported from a generated file. Here
 * we construct it inline to match the on-chain program exactly.
 */
function getProgram(provider: AnchorProvider): Program {
  // The IDL is auto-generated by `anchor build` into
  // target/idl/collateral_vault.json. At runtime we load it dynamically.
  // For Phase 1, we construct the Program instance using the workspace
  // pattern that @coral-xyz/anchor provides.
  //
  // In the test harness and scripts this is accessed via:
  //   const program = anchor.workspace.CollateralVault as Program;
  //
  // For the SDK, we accept any IDL-compatible Program.
  // This function is a convenience wrapper.

  // eslint-disable-next-line @typescript-eslint/no-var-requires
  let idl: Idl;
  try {
    // When running in the monorepo after `anchor build`, the IDL exists here
    idl = require("../../target/idl/collateral_vault.json");
  } catch {
    throw new Error(
      "IDL not found. Run `anchor build` first to generate target/idl/collateral_vault.json"
    );
  }

  return new Program(idl, provider);
}

