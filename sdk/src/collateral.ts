/**
 * Maps a ChainAsset enum value to the chain byte used by the on-chain program.
 * 0=BTC, 1=ETH, 2=SOL, 3=RWA
 */
function chainAssetToChainByte(chain: ChainAsset): number {
  switch (chain) {
    case ChainAsset.BtcNative:
      return 0;
    case ChainAsset.EthNative:
      return 1;
    case ChainAsset.SolNative:
      return 2;
    case ChainAsset.Usdc:
    case ChainAsset.TokenizedTBill:
    case ChainAsset.TokenizedRealEstate:
    case ChainAsset.TokenizedGold:
      return 3; // RWA bucket
    default:
      return 0;
  }
}

/**
 * Maps an on-chain chain byte back to the ChainAsset enum.
 */
function chainByteToChainAsset(chainByte: number): ChainAsset {
  switch (chainByte) {
    case 0:
      return ChainAsset.BtcNative;
    case 1:
      return ChainAsset.EthNative;
    case 2:
      return ChainAsset.SolNative;
    case 3:
      return ChainAsset.Usdc; // Default RWA to USDC bucket
    default:
      return ChainAsset.BtcNative;
  }
}

