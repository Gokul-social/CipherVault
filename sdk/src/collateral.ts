/**
 * Derives the VaultAccount PDA for a given owner.
 * Seeds: [b"vault", owner.toBytes()]
 */
function deriveVaultPda(
  owner: PublicKey,
  programId: PublicKey = COLLATERAL_VAULT_PROGRAM_ID
): [PublicKey, number] {
  return PublicKey.findProgramAddressSync(
    [Buffer.from("vault"), owner.toBuffer()],
    programId
  );
}

